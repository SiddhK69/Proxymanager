    Acronym = models.CharField(max_length=50, primary_key=True)
    Table_Id = models.IntegerField(max_length=2, unique=True)

    def save(self, *args, **kwargs):
        if not self.Table_Id:  # Only execute this code for new instances, not updates
            last_entry = TimeTable.objects.order_by('-Table_Id').first()
            if last_entry:
                self.Table_Id = last_entry.Table_Id + 1
            else:
                self.Table_Id = 1
        super(TimeTable, self).save(*args, **kwargs)

    
